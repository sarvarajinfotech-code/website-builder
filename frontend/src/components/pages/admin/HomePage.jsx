export default function HomePage() {
  const [file, setFile] = useState(null);
  const [previewUrl, setPreviewUrl] = useState(null);
  const [showWarning, setShowWarning] = useState(false);
  const { toast } = useToast();

  const handleFileChange = (event) => {
    const selectedFile = event.target.files[0];
    setFile(selectedFile);
    setShowWarning(false);

    if (selectedFile) {
      const reader = new FileReader();
      reader.onloadend = () => {
        setPreviewUrl(reader.result);
      };
      reader.readAsDataURL(selectedFile);
    } else {
      setPreviewUrl(null);
    }
  };

  const handleSubmit = (event) => {
    event.preventDefault();
    if (file) {
      // Here you would typically handle the file upload to your server
      toast({
        title: "Image Uploaded",
        description: `Successfully uploaded ${file.name}`,
      });
      setShowWarning(false);
    } else {
      setShowWarning(true);
      toast({
        title: "Error",
        description: "Please select an image to upload",
        variant: "destructive",
      });
    }
  };

  const handleNewProject = () => {
    // Logic to create a new project
    console.log("Creating a new project");
  };
  return (
    <div className="bg-white shadow-md rounded-lg p-4">
      <EmptyState
        heading="No projects"
        subheading="Get started by creating a new project."
        buttonText="New Project"
        onClick={handleNewProject}
        icon={<FolderPlus className="w-8 h-8" />}
      />
      {/* <div className="container mx-auto">
        <DataTable
          columns={columns}
          data={data}
          icon={<Plus />} // Pass the icon here
          buttonText="Add New Item" // Button text
          onButtonClick={() => console.log("Button clicked")} // Click handler
        />
      </div> */}
      <div className="space-y-4">
        <form onSubmit={handleSubmit} className="space-y-4">
          <div>
            <Label htmlFor="image">Select an image</Label>
            <Input
              id="image"
              type="file"
              accept="image/*"
              onChange={handleFileChange}
            />
          </div>
          <Button
            type="submit"
            className="flex items-center px-4 py-2 bg-blue-600 text-white rounded-md hover:bg-blue-700 focus:outline-none focus:ring-2 focus:ring-blue-500 focus:ring-opacity-50 transition duration-150 ease-in-out"
            onClick={() => {
              toast({
                title: "Scheduled: Catch up ",
                description: "Friday, February 10, 2023 at 5:57 PM",
                action: (
                  <ToastAction altText="Goto schedule to undo">
                    Undo
                  </ToastAction>
                ),
              });
            }}
          >
            Upload Image
          </Button>
        </form>

        {previewUrl && (
          <div className="mt-4">
            <h2 className="text-lg font-semibold mb-2">Image Preview</h2>
            <img
              src={previewUrl}
              alt="Preview"
              className="max-w-full h-auto max-h-64 rounded-lg shadow-md"
            />
          </div>
        )}
      </div>
    </div>
  );
}
